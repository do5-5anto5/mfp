numbers = [3, 10, 50, 30, 15]
print(numbers)
names = ["Joaquim", "Maria", "João", "Pedro"]
print(names)

# ordering small to big
numbers.sort()
print(numbers)

# ordering strings is automatically alphabetically
names.sort()
print(names)

# reverting the order
numbers.reverse()
print(numbers)
names.reverse()
print(names)
